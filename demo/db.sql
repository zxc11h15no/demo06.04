CREATE TABLE Patients (
    id INT PRIMARY KEY,
    first_name VARCHAR(50),
    last_name VARCHAR(50),
    patronymic VARCHAR(50),
    passport_number VARCHAR(20),
    passport_series VARCHAR(10),
    birth_date DATE,
    gender VARCHAR(10),
    address VARCHAR(100),
    phone_number VARCHAR(15),
    email VARCHAR(50),
    medical_card_number VARCHAR(20),
    medical_card_issue_date DATE,
    last_visit_date DATE,
    next_appointment_date DATE,
    insurance_policy_number VARCHAR(20),
    insurance_policy_expiry_date DATE
);

CREATE TABLE PatientContact (
    id INT PRIMARY KEY,
    patient_id INT,
    address VARCHAR(100),
    phone_number VARCHAR(15),
    email VARCHAR(50),
    FOREIGN KEY (patient_id) REFERENCES Patients(id)
);

CREATE TABLE PatientMedicalInfo (
    id INT PRIMARY KEY,
    patient_id INT,
    medical_card_number VARCHAR(20),
    medical_card_issue_date DATE,
    last_visit_date DATE,
    next_appointment_date DATE,
    insurance_policy_number VARCHAR(20),
    insurance_policy_expiry_date DATE,
    FOREIGN KEY (patient_id) REFERENCES Patients(id)
);
------------------------
CREATE TABLE MedicalProcedures (
    id INT PRIMARY KEY,
    patient_id INT,
    procedure_date DATE,
    doctor_name VARCHAR(50),
    procedure_type VARCHAR(50),
    procedure_name VARCHAR(50),
    procedure_results VARCHAR(50),
    procedure_price DECIMAL(10, 2),
    FOREIGN KEY (patient_id) REFERENCES Patients(id)
);
